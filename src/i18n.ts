  statusLabels[language][status] || statusLabels.en[status] || status;

export const getLocationLabel = (
  location: string | null | undefined,
  language: AppLanguage,
) => {
  if (location?.trim().toLocaleLowerCase() === "na putu") {
    return language === "nl"
      ? "Onderweg"
      : language === "bs"
        ? "Na putu"
        : "In transport";
  }

  return location || "";
};

